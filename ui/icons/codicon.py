class CodiconManager:
    _instance = None
    
    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._initialized = False
            cls._instance.font_family = 'Segoe MDL2 Assets'
        return cls._instance
    
    def init(self):
        if self._initialized:
            return
        
        self.font_path = os.path.join(os.path.dirname(__file__), 'codicon.ttf')
        loaded_family = self._load_font()
        if loaded_family:
            self.font_family = loaded_family
        self._initialized = True
    
    def _load_font(self):
        try:
            from PySide6.QtGui import QFontDatabase
            font_id = QFontDatabase.addApplicationFont(self.font_path)
            if font_id != -1:
                families = QFontDatabase.applicationFontFamilies(font_id)
                if families:
                    return families[0]
        except ImportError:
            pass
        return None
    
    def get_char(self, icon_name):
        code = CODEPOINTS.get(icon_name)
        if code:
            return chr(code)
        return '?'
    
    def get_font(self, size=14):
        try:
            from PySide6.QtGui import QFont
            font = QFont(self.font_family, size)
            return font
        except ImportError:
            return None
    
    def icon(self, icon_name, size=14):
        return self.get_char(icon_name)
    
    def font(self, size=14):
        return self.get_font(size)


codicon = CodiconManager()


def get_icon(icon_name):
    return codicon.get_char(icon_name)


def get_icon_font(size=14):
    return codicon.get_font(size)